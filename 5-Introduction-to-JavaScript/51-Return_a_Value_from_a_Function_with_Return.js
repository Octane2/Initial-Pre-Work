/* For this module, I learned how to define a function that accepts one argument,
and multiplies that value by 5. The variable is not defined as a private variable
with a local scope. The function then returns the result. */

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive (num1) {
  return num1 * 5;
}


console.log(minusSeven(10));
console.log(timesFive(5));
console.log(timesFive(2));
console.log(timesFive(0));
